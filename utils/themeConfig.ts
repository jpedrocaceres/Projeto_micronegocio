// Theme Registry
export const themes = {
  professionalBlue: professionalBlueTheme,
  corporateGreen: corporateGreenTheme,
  modernPurple: modernPurpleTheme,
  warmOrange: warmOrangeTheme,
  elegantGray: elegantGrayTheme,
};

// Current theme (change this to switch themes)
export const currentTheme = themes.professionalBlue;

// Theme utility functions
export function applyTheme(theme: ThemeColors) {
  const root = document.documentElement;
  
  // Apply primary colors
  Object.entries(theme.primary).forEach(([shade, color]) => {
    root.style.setProperty(`--color-primary-${shade}`, color);
  });
  
  // Apply neutral colors
  Object.entries(theme.neutral).forEach(([shade, color]) => {
    root.style.setProperty(`--color-neutral-${shade}`, color);
  });
  
  // Apply success colors
  Object.entries(theme.success).forEach(([shade, color]) => {
    root.style.setProperty(`--color-success-${shade}`, color);
  });
  
  // Apply warning colors
  Object.entries(theme.warning).forEach(([shade, color]) => {
    root.style.setProperty(`--color-warning-${shade}`, color);
  });
  
  // Apply error colors
  Object.entries(theme.error).forEach(([shade, color]) => {
    root.style.setProperty(`--color-error-${shade}`, color);
  });
}

export function getThemeColors(themeName: keyof typeof themes): ThemeColors {
  return themes[themeName];
}

// Theme switcher hook (for React components)
export function useThemeSwitcher() {
  const switchTheme = (themeName: keyof typeof themes) => {
    const theme = getThemeColors(themeName);
    applyTheme(theme);
    
    // Store theme preference
    localStorage.setItem('selected-theme', themeName);
  };
  
  const getCurrentTheme = (): keyof typeof themes => {
    return (localStorage.getItem('selected-theme') as keyof typeof themes) || 'professionalBlue';
  };
  
  return { switchTheme, getCurrentTheme };
}

